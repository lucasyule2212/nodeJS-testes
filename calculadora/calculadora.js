let calculadora = {
  sum: (intA, intB) => {
    return intA + intB;
  },
  mult: (intA, intB) => {
    return intA * intB;
  },
  div: (intA, intB) => {
    return intA / intB;
  },
  sub: (intA, intB) => {
    return intA - intB;
  },
  mod: (intA, intB) => {
    return intA % intB;
  },
}
export default calculadora;
